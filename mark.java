class mark{

	public static void main(String args[]){
	int marks=95;
	if(marks>=90)
		System.out.print("A+ Grade") ;
	else if(marks>=80)
		System.out.print("A Grade") ;
	else if(marks>=70)
		System.out.print("B+ Grade") ;	
	else if(marks>=60)
		System.out.print("B Grade") ;
	else if(marks>55)
		System.out.print("C Grade") ;
	else if(marks>=45&&marks<=55)
		System.out.print("P Grade") ;
	else
		System.out.print("F Grade") ;
}


}	